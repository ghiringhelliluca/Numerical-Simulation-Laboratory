#include <iostream>
#include <fstream>
#include "population.h"

using namespace std;

int main() {

  Population pop;
  pop.initialize();
  pop.sort(0);

  for (int gen = 0; gen < pop.get_generation(); gen++) {
    pop.evolve();
    pop.sort(gen+1);
  }

  return 0;
}


